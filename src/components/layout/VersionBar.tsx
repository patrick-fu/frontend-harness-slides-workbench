import { useEffect, useRef, useState } from "react";
import type { StyleRegistryEntry } from "../../types";

export interface VersionBarProps {
  style: StyleRegistryEntry;
  currentVersionId: string;
  language: "en" | "zh";
  onGoOverview: () => void;
  onSelectVersion: (versionId: string) => void;
}

export default function VersionBar({
  style,
  currentVersionId,
  language,
  onGoOverview,
  onSelectVersion,
}: VersionBarProps) {
  const [menuOpen, setMenuOpen] = useState(false);
  const menuRef = useRef<HTMLDivElement>(null);
  const currentVersionIndex = style.versions.findIndex(
    (v) => v.id === currentVersionId,
  );
  const activeVersionIndex =
    currentVersionIndex >= 0 ? currentVersionIndex : 0;
  const currentVersion = style.versions[activeVersionIndex];
  const totalVersions = style.versions.length;

  if (!currentVersion) return null;

  const styleName = style.name[language];
  const hasMultiple = totalVersions > 1;
  const activeVersionId = currentVersion.id;
  const switcherLabel = language === "zh" ? "版本" : "Versions";
  const currentPosition = `v${activeVersionIndex + 1}/${totalVersions}`;
  const currentTopic = currentVersion.topic[language];

  useEffect(() => {
    if (!menuOpen) return;

    const handleMouseDown = (event: MouseEvent) => {
      if (!menuRef.current?.contains(event.target as Node)) {
        setMenuOpen(false);
      }
    };

    const handleKeyDown = (event: KeyboardEvent) => {
      if (event.key === "Escape") setMenuOpen(false);
    };

    document.addEventListener("mousedown", handleMouseDown);
    document.addEventListener("keydown", handleKeyDown);
    return () => {
      document.removeEventListener("mousedown", handleMouseDown);
      document.removeEventListener("keydown", handleKeyDown);
    };
  }, [menuOpen]);

  return (
    <div
      data-testid="version-bar"
      className="h-7 flex items-center px-3 gap-2 text-xs border-b border-ink/10 bg-chrome/60"
      style={{ flexShrink: 0 }}
    >
      <button
        type="button"
        onClick={onGoOverview}
        className="min-w-0 max-w-[220px] flex items-center gap-1.5 text-ink/60 hover:text-ink transition-colors"
        title={language === "zh" ? "返回总览" : "Return to overview"}
      >
        <span className="font-mono text-[11px]">{style.id}</span>
        <span className="text-ink/20">·</span>
        <span className="font-medium truncate max-w-[160px]">
          {styleName}
        </span>
      </button>

      <svg
        width="12"
        height="12"
        viewBox="0 0 12 12"
        fill="none"
        stroke="currentColor"
        strokeWidth="1.5"
        strokeLinecap="round"
        strokeLinejoin="round"
        className="text-ink/20 shrink-0"
      >
        <polyline points="4,2 8,6 4,10" />
      </svg>

      <div className="hidden sm:flex min-w-0 items-center gap-2">
        <span className="text-ink/80 font-medium truncate max-w-[200px]">
          {currentTopic}
        </span>
        <span
          className="text-[10px] px-1.5 py-0.5 rounded bg-ink/[0.06] text-ink/50 font-mono shrink-0"
          title={`Generated by ${currentVersion.model}`}
        >
          {currentVersion.model}
        </span>
      </div>

      <div className="flex-1" />

      {hasMultiple ? (
        <div
          ref={menuRef}
          className="relative shrink-0"
          onMouseEnter={() => setMenuOpen(true)}
          onMouseLeave={() => setMenuOpen(false)}
        >
          <button
            type="button"
            data-testid="version-switcher"
            aria-haspopup="menu"
            aria-expanded={menuOpen}
            aria-label={switcherLabel}
            onClick={() => setMenuOpen(true)}
            className="h-6 max-w-[150px] px-2 rounded-md border border-ink/10 bg-ink/[0.05] hover:bg-ink/[0.09] flex items-center gap-1.5 text-ink/70 transition-colors"
            title={language === "zh" ? "切换版本" : "Switch version"}
          >
            <span className="hidden sm:inline text-[10px] font-medium">
              {switcherLabel}
            </span>
            <span className="font-mono text-[10px] shrink-0">
              {currentPosition}
            </span>
            <svg
              width="10"
              height="10"
              viewBox="0 0 10 10"
              fill="none"
              stroke="currentColor"
              strokeWidth="1.4"
              strokeLinecap="round"
              strokeLinejoin="round"
              className="shrink-0"
              style={{
                transform: menuOpen ? "rotate(180deg)" : "rotate(0deg)",
              }}
            >
              <polyline points="2,3.5 5,6.5 8,3.5" />
            </svg>
          </button>

          {menuOpen && (
            <div
              data-testid="version-menu-hover-zone"
              className="absolute right-0 top-full w-72 max-w-[calc(100vw-1rem)] pt-1 z-50"
            >
              <div
                data-testid="version-menu"
                role="menu"
                className="rounded-md border border-ink/10 bg-elevated shadow-lg p-1"
              >
                {style.versions.map((version, index) => {
                  const isActive = version.id === activeVersionId;
                  const label =
                    version.id === `v${index + 1}` ? `v${index + 1}` : version.id;

                  return (
                    <button
                      key={version.id}
                      type="button"
                      role="menuitem"
                      data-testid={`version-option-${version.id}`}
                      aria-current={isActive ? "true" : undefined}
                      aria-pressed={isActive}
                      onClick={() => {
                        if (!isActive) onSelectVersion(version.id);
                        setMenuOpen(false);
                      }}
                      className={[
                        "w-full min-w-0 px-2.5 py-2 rounded-[5px] flex items-center gap-2 text-left transition-colors",
                        isActive
                          ? "bg-ink/[0.09] text-ink"
                          : "text-ink/65 hover:bg-ink/[0.06]",
                      ].join(" ")}
                    >
                      <span className="w-3 text-center text-[10px] text-ink/45">
                        {isActive ? "*" : ""}
                      </span>
                      <span className="font-mono text-[10px] text-ink/55 shrink-0">
                        {label}
                      </span>
                      <span className="min-w-0 flex-1 truncate text-[11px]">
                        {version.topic[language]}
                      </span>
                      <span className="font-mono text-[9px] text-ink/35 shrink-0">
                        {version.model}
                      </span>
                    </button>
                  );
                })}
              </div>
            </div>
          )}
        </div>
      ) : (
        <span
          data-testid="version-count"
          className="text-[10px] font-mono text-ink/30 shrink-0"
        >
          v{activeVersionIndex + 1}/{totalVersions}
        </span>
      )}
    </div>
  );
}
